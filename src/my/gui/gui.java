    private void BotonOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonOKActionPerformed
        // TODO add your handling code here:
        usuario = UsuarioBD.getText();
        nombrebd = NombreBD.getText();
        password = PasswordBD.getText();
        columna = ColumnaBD.getText();
        certeza = Integer.valueOf(Certeza.getValue().toString());
        UsuarioBD.setEnabled(false);
        NombreBD.setEnabled(false);
        PasswordBD.setEnabled(false);
        ColumnaBD.setEnabled(false);
        Certeza.setEnabled(false);
        BotonGrafico.setEnabled(true);

        if (Especifico.isEnabled() == true){
            datosDiscretizar = DatosDiscretizacion.getText();
            datosDiscretizar = datosDiscretizar.trim();
            
            for (int i=0; i< datosDiscretizar.length(); i++){
                //parsear el string
                c = datosDiscretizar.substring(i, i+1);

                if ((c.compareTo(" ") == 0) || (c.compareTo("\n") == 0) || (c.compareTo("\t") == 0)){
                    //ignorar
                } else
                if (c.compareTo(",") == 0){
                    columnas.add(aux);
                    aux = "";
                } else if (c.compareTo(";") == 0){
                    valores.add(aux);
                    aux = "";
                } else {
                    aux = aux.concat(c);
                }
            }
            //System.out.println("Columnas: " + columnas + "\nValores: " + valores);
        }

        Promedio.setEnabled(false);
        Especifico.setEnabled(false);
        DatosDiscretizacion.setEnabled(false);

	DataBase.columna_decision=columna;

        //Cargar los datos de la BD para mostrar en el Panel de Datos
        TextoDatos.setText("\n  Base de Datos: ");
        TextoDatos.append(nombrebd);
        TextoDatos.append("\n  Usuario: ");
        TextoDatos.append(usuario);
        TextoDatos.append("\n\n  Información extraida de la Base de Datos: \n");
        
        DataBase datos = new DataBase();
        datos.Conectar(nombrebd,usuario, password);

        try{
            datos.getNombresColumnas();
	    tree.generar_arbol(columna_arbol, valores_decision, 1.0);
            //map = datos.ParseoDB(columna_arbol,valor_columna_arbol);
	    //valores_decision=datos.getCantidadValores(columna_arbol, valor_columna_arbol);
            //System.out.println(tree.calcular_nodo(map,valores_decision));
            datos.getValoresPantalla();
        }
        catch(SQLException e){
            System.out.println("Error al parsear: " + e);
        }
        TextoDatos.append("      ");
        for(int i = 0; i < datos.data.size(); i++){
            TextoDatos.append(datos.data.get(i).toString());
        }
        
        //Generar el Log
        TextoLog.setText("");

        for(int i = 0; i < log.datosLog.size(); i++){
            TextoLog.append(log.datosLog.get(i).toString());
        }
        //arbolTexto();
        datos.Desconectar();
        TextoLog.append("  " + new Date() + "\tTermino de la Ejecucion\n");
    }//GEN-LAST:event_BotonOKActionPerformed

